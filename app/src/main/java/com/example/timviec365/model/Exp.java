package com.example.timviec365.model;

public class Exp {
    private String IdExp;
    private String NameExp;

    public Exp(String idExp, String nameExp) {
        IdExp = idExp;
        NameExp = nameExp;
    }

    public String getIdExp() {
        return IdExp;
    }

    public void setIdExp(String idExp) {
        IdExp = idExp;
    }

    public String getNameExp() {
        return NameExp;
    }

    public void setNameExp(String nameExp) {
        NameExp = nameExp;
    }
}


